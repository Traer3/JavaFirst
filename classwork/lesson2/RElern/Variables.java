package com.hillel.classwork.lesson2.RElern;

public class Variables {

    public static void main(String[] args) {

        byte myBite = 19;
        System.out.println(myBite);

        short mySort = 30000;
        System.out.println(mySort);

        System.out.println();
        System.out.println(myBite + mySort);
        int result = myBite + mySort;
        System.out.println(result);
        System.out.println();

        int myInt = 3200000;
        System.out.println(myInt);

        long myLong = 32000002000000000L;
        System.out.println(myLong);

        float myFloat = 32.1213F;
        System.out.println(myFloat);

        double myDouble = 123.12313;
        System.out.println(myDouble);

        boolean isValid = false;
        System.out.println(isValid);

        char myChar = '&';
        System.out.println(myChar);

        char myFirstChar = 12312;
        System.out.println(myFirstChar);

        char mySecondChar = '\u0054';
        System.out.println(mySecondChar);

        String stroka = "test";
        System.out.println(stroka);
    }
}
