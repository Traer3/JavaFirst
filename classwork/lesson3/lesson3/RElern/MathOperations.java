package com.hillel.classwork.lesson3.RElern;

public class MathOperations {
    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        int c = a+b;

        System.out.println("Конкатенация " + c);
        System.out.println("a + b = " + c);
        System.out.println("a - b = " + (a-b));
        System.out.println("a * b = " + (a*b));
        System.out.println("a / b = " + (a/b));
        System.out.println("a % b = " + (a%b));
        System.out.println("Постинкремент");
        System.out.println("a ++ = " + a++);
        System.out.println(a);
        System.out.println("Пост декремент");
        System.out.println("a -- = " + a--);
        System.out.println(a);
        System.out.println("Преинкремент");
        System.out.println("++b = " + ++b);
        System.out.println("Пре декремент");
        System.out.println("--b = " + --b);




    }
}
