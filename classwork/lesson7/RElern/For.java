package com.hillel.classwork.lesson7.RElern;

public class For {
    public static void main(String[] args) {


//проверка на кратность
        for (int i = 0; i < 100; i++) {
            if (i % 4 == 0 || i % 5 == 0) {
                System.out.println(i);
            }

        }
    }
}
